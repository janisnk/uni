package labyrinth;

import java.awt.Image;

/**
 *Finish of the level
 * Represented by a flag img on GUI
 * @author sinko
 */
public class Finish extends Sprite{

    public Finish(int x, int y, int width, int height, Image image) {
        super(x, y, width, height, image);
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public Image getImage() {
        return image;
    }

    public void setImage(Image image) {
        this.image = image;
    }
   
    
}
